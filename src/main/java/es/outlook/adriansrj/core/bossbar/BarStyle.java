package es.outlook.adriansrj.core.bossbar;

/**
 * An enumeration for the styles the boss bars supports.
 * <p>
 * @author AdrianSR / Tuesday 14 April, 2020 / 09:32 AM
 */
public enum BarStyle {
	
    /**
     * Makes the boss bar solid (no segments)
     */
    SOLID,
    
    /**
     * Splits the boss bar into 6 segments
     */
    SEGMENTED_6,
    
    /**
     * Splits the boss bar into 10 segments
     */
    SEGMENTED_10,
    
    /**
     * Splits the boss bar into 12 segments
     */
    SEGMENTED_12,
    
    /**
     * Splits the boss bar into 20 segments
     */
    SEGMENTED_20,
}