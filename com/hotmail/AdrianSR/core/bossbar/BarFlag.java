package com.hotmail.AdrianSR.core.bossbar;

/**
 * Represents the BossBars flags.
 * <p>
 * @author AdrianSR
 */
public enum BarFlag {

    /**
     * Darkens the sky like during fighting a wither.
     */
    DARKEN_SKY,
    /**
     * Tells the client to play the Ender Dragon boss music.
     */
    PLAY_BOSS_MUSIC,
    /**
     * Creates fog around the world.
     */
    CREATE_FOG,
}
